				Just like there are many different dimensions to effective personal communication, there are many different dimensions to customer communication.

So just like you wouldn't talk to your child like you would a buddy from college, you shouldn't have the same communication practice for all customers.

Here are a few steps to get started with customer segmentation, and evolve from a general marketing approach to a targeted and personalized customer relationship strategy (this approach can be used for both B-to-C and B-to-B businesses).
 
<h4>Start with the broadest category definitions.</h4>
You can begin to segment you customer base by lifecycle stages. There are 3 broad categories that any customer base can be organized by:
<p>New, Active, and Churn.</p>
<img class="churmimgs" src="img/BellCurve.png"/>

<div class="col-md-4 col-xs-12 col-sm-4">
<span style="color: #576a78;"><b>New Customers</b></span> are individuals who have recently made their first purchase (or other transaction). They are taking you for a test drive, no basis for trust has been established yet, and they are most relatively sensitive to all aspects of their experience with your organization (website experience, digital presence, customer service, etc.)
</div>

<div class="col-md-4 col-xs-12 col-sm-4">
<span style="color: #576a78;"><b>Active Customers</b></span> are those who have been recently active beyond a first purchase or predefined new customer engagement period. The degree to which they are active will be further defined when we dig deeper into sub-segmentation. The range can include brand advocates, repeat customers, and discount/special offer purchasers.
</div>

<div class="col-md-4 col-xs-12 col-sm-4">
<span style="color: #576a78;"><b>Customers in the churn stage</b></span> of the lifecycle have been inactive or not made a purchase for an extended period of time.
</div>
